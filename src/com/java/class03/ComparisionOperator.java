package com.java.class03;

public class ComparisionOperator {
	public static void main(String[] args) {

		int a = 10, b = 11;
		
		// Comparison operator always return boolean 
		System.out.println(a == b);
		System.out.println(a != b);
		System.out.println(a >= b);
		System.out.println(a <= b);
		System.out.println(a > b);
		System.out.println(a < b);
		
	}

}
